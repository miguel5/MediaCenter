    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nome = this.jTextField1.getText();
        String email = this.jTextField2.getText();
        char[] p = jPasswordField1.getPassword();
        String password = String.valueOf(p);
        
        if(nome.equals("") || email.equals("") || password.equals(""))
            this.jLabel4.setVisible(true);
        
        else{
            try {
                this.uif.registarUtilizador(nome, email, password);
            } catch (SQLException ex) {
                Logger.getLogger(RegistarUtilizadorUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            this.dispose();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

